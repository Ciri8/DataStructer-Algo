        System.out.println("******************************");

        // Print the empty line
        System.out.println("|                            |");

        // Print the store name
        System.out.println("|         MCLEAN STORE       |");

        // Print the empty line
        System.out.println("|                            |");

        // Print the date and time
        System.out.println("| 12/15/2023         2:45 PM |");

